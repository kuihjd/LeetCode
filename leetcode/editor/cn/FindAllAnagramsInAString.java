class Solution {
    public List<Integer> findAnagrams(String s, String p) {
        List<Integer> ans = new ArrayList<>();
        if (p.length() > s.length()) {
            return ans;
        }
        int[] cnt = new int[26];
        int[] cur = new int[26];
        for (char i : p.toCharArray()) {
            cnt[i - 'a']++;
        }
        char[] c = s.toCharArray();
        int n = p.length();
        for (int i = 0; i < n; ++i) {
            cur[c[i] - 'a']++;
        }
        for (int i = n; i < c.length; ++i) {
            if (Arrays.equals(cnt, cur)) {
                ans.add(i - n);
            }
            cur[c[i] - 'a']++;
            cur[c[i - n] - 'a']--;
        }
        if (Arrays.equals(cnt, cur)) {
            ans.add(c.length - n);
        }
        return ans;
    }
}
